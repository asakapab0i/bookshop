About labelle aurore bookstore
About
La Belle Aurore Bookshop is a second-hand bookstore located in Hernan Cortes St., Mandaue City, Cebu. It was established in December 2009. It currently carries more than seven thousand volumes of used and overstock books.
Description
Mandaue City: Hernan Cortes St., Banilad

Cebu City: G/F Honey Hunt Pension House, Junquera Ext., Brgy San Antonio
General Information

The name La Belle Aurore is taken from a French bistro in the 1940's film Casablanca. La Belle Aurore means the beautiful daybreak or the beautiful sunrise. Aurore could also serve as a woman's name, as with Aurora; the beautiful Aurore.

Let's take time and encourage people, especially the youth, to read, not only to be taken as a form of recreation, but also as a privilege to develop reason and inquiry, and habituate our minds to critical thought.

 
